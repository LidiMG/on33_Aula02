# Adição
soma = 5 + 3
print(soma) # O resultado deverá ser 8

# Subtração
subtracao = 5 - 3
print(subtracao)  # O resultado deverá ser 2

# Multiplicação
multiplicacao = 5 * 3 
print(multiplicacao) # O resultado deverá ser 15

# Divisão
divisao = 5 / 3 
print(divisao) # O resultado deverá ser 1.666

# Divisão inteira
divisao_interira = 5 // 3 
print(divisao_interira) # O resultado deverá ser 1

# Módulo (sobra do resultado)
modulo = 20%6
print(modulo) # O resultado deverá ser 2

# Exponencial (elevado a potência)
exponencial = 2 ** 3
print(exponencial) # O resultado deverá ser 8